DROP TABLE IF EXISTS booking;
DROP TABLE IF EXISTS trip;
DROP TABLE IF EXISTS driver;
DROP TABLE IF EXISTS shuttle;
DROP TABLE IF EXISTS guest;
DROP TABLE IF EXISTS destination;
DROP TABLE IF EXISTS hotel;
DROP TABLE IF EXISTS airport;
CREATE TABLE airport(id INTEGER PRIMARY KEY, name VARCHAR(50) NOT NULL, adresse VARCHAR(50) NOT NULL);
CREATE TABLE hotel(id INTEGER PRIMARY KEY, name VARCHAR(50) NOT NULL, address VARCHAR(50) NOT NULL );
CREATE TABLE destination( id INTEGER PRIMARY KEY, hotel REFERENCES hotel, airport REFERENCES airport);
CREATE TABLE guest( id INTEGER PRIMARY KEY, firstname VARCHAR(50) NOT NULL, lastname VARCHAR(50) NOT NULL, hotel INTEGER NOT NULL REFERENCES hotel);
CREATE TABLE shuttle( id INTEGER NOT NULL PRIMARY KEY, name VARCHAR(50) NOT NULL, airport INTEGER NOT NULL REFERENCES airport, seats INTEGER NOT NULL);
CREATE TABLE driver( id INTEGER NOT NULL PRIMARY KEY, firstname VARCHAR(50) NOT NULL, lastname VARCHAR(50) NOT NULL, shuttle REFERENCES shuttle, airport NOT NULL REFERENCES airport, available BOOLEAN NOT NULL );
CREATE TABLE trip ( id INTEGER PRIMARY KEY, "to" INTEGER NOT NULL REFERENCES destination, "from" INTEGER NOT NULL REFERENCES destination, time VARCHAR(50) NOT NULL, shuttle INTEGER REFERENCES shuttle );
CREATE TABLE booking ( id INTEGER PRIMARY KEY, person INTEGER NOT NULL REFERENCES guest, shuttle INTEGER NOT NULL REFERENCES shuttle, arrival INTEGER NOT NULL REFERENCES trip, arrivaldate DATE NOT NULL, departure INTEGER NOT NULL REFERENCES trip, departuredate DATE NOT NULL );
